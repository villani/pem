// Definindo um vetor com um elemento.
var religiao = ['católica'];

// Inserindo o segundo elemento no final do vetor.
// push(valor) - empurrar - elemento inserido no final da fila. 
religiao.push('evangélica');

// Inserindo o terceiro elemento no início do vetor.
// unshift(valor) - deslocando - os elementos anteriores de posição - inseri no ínicio
religiao.unshift('islâmica');

console.log(religiao);
